package Oct.ex_23092024;

public class Lab040_Switch_Statement {
    public static void main(String[] args) {
         int a = 1;
         switch (a){
             case 1:
                 System.out.println("monday");
                 break;

             case 2:
                 System.out.println("Tue");
                 break;

             case 3:
                 System.out.println("wed");
                 break;

             case 4:
                 System.out.println("fri");
                 break;

         }
    }
}
