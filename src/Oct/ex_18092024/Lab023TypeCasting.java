package Oct.ex_18092024;

public class Lab023TypeCasting {
    public static void main(String[] args) {
        byte a = 10;
        byte b = 20;
        int c = a+b;
        System.out.println(c);


        char m = 'C';
        char p = 'H';
        int s = m+p;
        System.out.println(m);
        System.out.println(p);

        System.out.println(s);

    }
}
