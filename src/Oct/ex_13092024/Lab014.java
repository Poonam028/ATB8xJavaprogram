package Oct.ex_13092024;


public class Lab014 {
    public static void main(String[] args) {
        System.out.printf("%-10s %-10s %n", "Name", "Age");
        System.out.printf("%-10s %-10d %n", "John", 30);
        System.out.printf("%-10s %-10d %n", "Jane", 25);
        System.out.printf("%-10s %-10s %n", "Name", "Age");
        System.out.printf("%-10s %-10d %n", "John", 30);
        System.out.printf("%-10s %-10d %n", "Jane", 25);

        System.out.printf("Current date: %tD%n", new java.util.Date());
        double pi = 3.14159;
        System.out.printf("Pi rounded to two decimals: %.2f%n", pi);
        // %.2f%n == print the value until two decimals

       

    }
}
