package Task;

public class task_2592024_10_Table {
    public static void main(String[] args) {
     int num = 10;
     //Using for loop
     for (int i =1; i<=10; i++){
         System.out.println(num*i);

        }

     //Using while loop
     int i =1;
     while(i<=10){
         System.out.println(num*i);
         i++;
        }
    }
}
