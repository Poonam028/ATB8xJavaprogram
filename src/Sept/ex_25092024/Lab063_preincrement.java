package Sept.ex_25092024;

public class Lab063_preincrement {
    public static void main(String[] args) {
        for (int i = 0; i <= 50; i++) {
            System.out.println(i);
        }
        for (int i = 0; i <= 50; ++i) {
            System.out.println(i);
        }
        for (char a = 'A'; a <= 'm'; ++a) {
            System.out.println(a);
        }
    }

}